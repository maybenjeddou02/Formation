// Déclaration des deux nombres
let nombre1 = 10; 
let nombre2 = 15; 

// Condition pour vérifier si les deux nombres sont égaux
if (nombre1 === nombre2) {
    console.log("Les nombres sont égaux");
} else {
    console.log("Les deux nombres sont différents");
    console.log("La différence entre les deux nombres est : " + Math.abs(nombre1 - nombre2));
}
