// Affectation de la variable genre
let genre = "garçon"; // ou "fille" 

// Condition pour vérifier le genre et afficher le message correspondant
if (genre === "garçon") {
    console.log("Je suis un garçon !");
} else if (genre === "fille") {
    console.log("Je suis une fille !");
} else {
    console.log("Genre non spécifié !");
}
